export function getActiveQuest(quests: RewardQuest[]): RewardQuest {
  return (
    quests.find((q) => q.isActiveQuest) ??
    quests.find((q) => q.status === "in_progress" || q.status === "claimable") ??
    quests[0]
  );
}

